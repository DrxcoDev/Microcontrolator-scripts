// Definiciones de puertos
#define PORTB   _SFR_IO8(0x05)  // Dirección del puerto B
#define DDRB    _SFR_IO8(0x04)  // Dirección del registro de dirección de datos del puerto B
#define PINB    _SFR_IO8(0x03)  // Dirección del registro de entrada del puerto B

// Definiciones de bits para PORTB
#define PB0     0  // Bit 0 del puerto B
#define PB1     1  // Bit 1 del puerto B
#define PB2     2  // Bit 2 del puerto B
// ...y así sucesivamente para otros bits y puertos
